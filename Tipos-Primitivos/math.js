//------------Arredondamento Numerico --------------

const v1 = Math.ceil(3.5); //retorna valor para cima.
console.log(v1); // 4

const v2 = Math.floor(3.5); // retorna valor para baixo.
console.log(v2); //3

const v3 = Math.round(3.3); // aredonda numeros.
console.log(v3); //3

const v4 = Math.abs(15); // returna numeros absolutos.
console.log(v4); //15

const v5 = Math.sqrt(64); // raiz quadrada.
console.log(v5); //8

const v6 = Math.pow(3, 2); // potenciação.
console.log(v6); //9

const v7 = Math.max(1, 2, 10, 4, 25, 2); // returna o MAIOR Valor.
console.log(v7); //25

const v8 = Math.min(1, 2, 10, 4, 25, 2); // return o MENOR valor.
console.log(v8); // 1
